""" 指令 """
# - 1. pyinstaller -F <filename.py>   生成文件在dist目录下
# - 2. -h 查看帮助
# - 3. --clean 清理打包过程中产生的临时文件
# - 4. -D/--onedir  默认。生产dist文件夹
# - 5. -F/--onefile 在dist文件夹中只生成独立的可执行文件
# - 6. -i <path/iconfilname.ico> 指定打包程序使用的图标文件
